package com.loiane.cursojavabasico.aula11;

public class VariaveisPontoFlutuante {

	public static void main(String[] args) {
		
		double valorPassagem = 2.90;
		
		float valorTomate = 3.95F;
		
		System.out.println("Valor da passagem = " + valorPassagem);
		System.out.println("Valor do tomate = " + valorTomate);
		
		double d1 = 123.4;
		
		// mesmo valor de d1, mas em notação científica, ou seja, potência de 10
		double d2 = 1.234e2;
		
		System.out.println("Valor de d1 = " + d1);
		System.out.println("Valor de d2 = " + d2);
		
	}

}
