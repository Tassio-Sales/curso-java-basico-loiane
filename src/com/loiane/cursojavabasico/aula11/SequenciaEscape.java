package com.loiane.cursojavabasico.aula11;

public class SequenciaEscape {

	public static void main(String[] args) {
		
		
		//"Hello, World!"
		
		// \t para dar tab na string
		// \" para adicionar aspas duplas na string
		// \' para adicionar aspas somples na string
		// \n quebra linha
		// \r retorno de carro = pula linha		
		// \\ para colocar uma barra invertida na string
		
		
		
		System.out.println("\"Hello, World!\"");
		
		// 1\4
		
		System.out.println("1\\4");

	}

}
